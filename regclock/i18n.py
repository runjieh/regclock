_LABELS: dict[str, dict[str, str]] = {"en": dict(_BUILTIN_LABELS_EN)}
_TEMPLATE_DIRS: dict[str, Path] = {}
_default_lang: str = DEFAULT_LANG


def register_language(
    lang: str,
    labels: dict[str, str],
    templates_dir: Path | str | None = None,
) -> None:
    """Register (or extend) a language pack at runtime.

    Args:
        lang: ISO 639-1 code (e.g. ``"es"``, ``"zh"``). Stored
            lower-cased.
        labels: Flat ``key -> translated string`` mapping. Missing keys
            fall back to English at lookup time. Calling this twice for
            the same language **merges** keys (later wins).
        templates_dir: Optional path to a directory containing
            translated Jinja templates. File names must match the
            English pack (``filing_stub.md.j2``,
            ``reminder_body.md.j2``); missing files fall back to the
            English template.

    Example:
        >>> register_language("es", {"ui.bearer": "Sujeto obligado"})

    """
    lang = lang.lower()
    bucket = _LABELS.setdefault(lang, {})
    bucket.update(labels)
    if templates_dir is not None:
        _TEMPLATE_DIRS[lang] = Path(templates_dir)


def set_default_language(lang: str) -> None:
    """Set the default language used when no ``lang`` is passed at call sites."""
    global _default_lang
    _default_lang = lang.lower()


def get_default_language() -> str:
    """Return the current default language code."""
    return _default_lang


def available_languages() -> list[str]:
    """List language codes with at least one registered label."""
    return sorted(_LABELS.keys())


def t(key: str, lang: str | None = None) -> str:
    """Look up a display string.

    Resolution order: requested language → English → the key itself
    (so untranslated keys remain visible to translators rather than
    failing silently).

    Args:
        key: Dotted label key, e.g. ``"ui.bearer"`` or
            ``"state.satisfied"``.
        lang: Language code; defaults to the current default language.

    Returns:
        The translated string, or the key as a placeholder when no
        translation is registered in any language.

    """
    lang = (lang or _default_lang).lower()
    if (val := _LABELS.get(lang, {}).get(key)) is not None:
        return val
    if (val := _LABELS["en"].get(key)) is not None:
        return val
    return key


def label_for_state(state: State, lang: str | None = None) -> str:
    """Translate a :class:`State` enum member for display.

    The enum's ``.value`` itself is never translated; this returns the
    *display* string only. JSON serialisation continues to use
    ``state.value`` (e.g. ``"satisfied"``).
    """
    return t(f"state.{state.value}", lang)


def label_for_event_kind(kind: EventKind, lang: str | None = None) -> str:
    """Translate an :class:`EventKind` enum member for display."""
    return t(f"event_kind.{kind.value}", lang)


def templates_dir_for(lang: str) -> Path | None:
    """Return the registered templates directory for ``lang`` if any.

    Returns ``None`` when the language has no custom template directory
    (callers should then fall back to the English template pack).
    """
    return _TEMPLATE_DIRS.get(lang.lower())


def labels_for_jinja(lang: str | None = None) -> dict[str, str]:
    """Return the label dict for use as a Jinja ``ui`` namespace.

    Keys with the ``ui.`` prefix are exposed without the prefix so
    templates can write ``{{ ui.bearer }}`` instead of
    ``{{ ui['ui.bearer'] }}``. Other namespaces (``state.``,
    ``event_kind.``, ``report.``) are exposed via the helper
    functions registered as Jinja globals.
    """
    lang = (lang or _default_lang).lower()
    merged: dict[str, str] = {}
    merged.update(_LABELS["en"])
    merged.update(_LABELS.get(lang, {}))
    return {
        key.removeprefix("ui."): value
        for key, value in merged.items()
        if key.startswith("ui.")
    }


