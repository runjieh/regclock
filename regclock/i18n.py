"""Internationalisation (i18n) registry for display strings and templates.

regclock keeps machine-readable values (enum ``.value``, JSON event-log
keys, evidence-pack digests, scheduled-instance identifiers) in
**English/ASCII** so that serialised artefacts remain interoperable
across locales. Only *display* strings — labels in reports, headings
in filing stubs, reminder bodies — are translated.

Two things are translatable:

* **labels** — a flat ``dict[str, str]`` keyed by dotted names
  (e.g. ``"ui.bearer"``, ``"state.satisfied"``). Looked up at render
  time via :func:`t`, :func:`label_for_state`, and
  :func:`label_for_event_kind`.
* **templates** — Jinja2 ``.md.j2`` / ``.html.j2`` files. The default
  pack lives under ``regclock/templates/en/``. Language packs can ship
  their own templates by registering a directory via
  :func:`register_language`; missing template names fall back to the
  English pack.

Usage:

    >>> from regclock import i18n
    >>> i18n.register_language("es", labels={
    ...     "ui.bearer": "Sujeto obligado",
    ...     "state.satisfied": "Cumplida",
    ...     ...
    ... })
    >>> i18n.set_default_language("es")
    >>> # or pass lang="es" explicitly to render functions

Anything not registered falls back to English, then to the key itself.
The fallback is loud-by-design only for missing translations (returns
the raw key), so missing strings are visible to translators.
"""

